export type SpokenLanguage = (typeof SPOKEN_LANGUAGES)[number];

const LANGUAGE_SET = new Set<string>(SPOKEN_LANGUAGES);

export function parseLanguages(value: string): string[] {
  if (!value.trim()) return [];
  return value
    .split(",")
    .map((part) => part.trim())
    .filter(Boolean);
}

export function serializeLanguages(languages: string[]): string {
  return languages
    .map((lang) => lang.trim())
    .filter(Boolean)
    .sort((a, b) => a.localeCompare(b))
    .join(", ");
}

/** Normalize free-text legacy values into known language names where possible */
export function normalizeLanguages(value: string): string {
  const parts = parseLanguages(value);
  const normalized = parts.filter((part) => LANGUAGE_SET.has(part));
  const unknown = parts.filter((part) => !LANGUAGE_SET.has(part));
  return serializeLanguages([...normalized, ...unknown]);
}
